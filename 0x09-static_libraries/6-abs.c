/**
 * _abs - Function that computes the abosolute value of a int
 * @n: number value positive or negative
 *
 * Return: Returns n when is positive, c when is negative
 */
int _abs(int n)
{

	int x;
	int c;

	x = -1;
	c = n * x;

	if (n >= 0)
	{
	return (n);
	}
	else
	{
	return (c);
	}
}
